package com.project.covidapp.rest;

public class USCaseNotFoundException extends RuntimeException {

	public USCaseNotFoundException() {
		
	}

	public USCaseNotFoundException(String arg0) {
		super(arg0);
		
	}

	public USCaseNotFoundException(Throwable arg0) {
		super(arg0);
		
	}

	public USCaseNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public USCaseNotFoundException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
